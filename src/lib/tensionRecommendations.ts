// Получить рекомендации для T1
export function getT1Recommendations(delta: number): TensionRecommendations {
  const direction = delta > 0 ? 'AT' : 'AM';
  return {
    universal: T1_UNIVERSAL,
    directional: T1_DIRECTIONAL[direction],
    microActions: T1_MICRO_ACTIONS[direction]
  };
}

// Получить рекомендации для T2
export function getT2Recommendations(delta: number): TensionRecommendations {
  const direction = delta > 0 ? 'CT' : 'TU';
  return {
    universal: T2_UNIVERSAL,
    directional: T2_DIRECTIONAL[direction],
    microActions: T2_MICRO_ACTIONS[direction]
  };
}

